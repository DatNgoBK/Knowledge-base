package model;

public class LevelRule {
	String id;
	int level;
	boolean test;
	double mult;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public boolean isTest() {
		return test;
	}
	public void setTest(boolean test) {
		this.test = test;
	}
	public double getMult() {
		return mult;
	}
	public void setMult(double mult) {
		this.mult = mult;
	}
	
	
}
