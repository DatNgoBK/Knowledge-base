package model;

public class Credit {
	String id;
	double cpa;
	int credit;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public double getCpa() {
		return cpa;
	}
	public void setCpa(double cpa) {
		this.cpa = cpa;
	}
	public int getCredit() {
		return credit;
	}
	public void setCredit(int credit) {
		this.credit = credit;
	}
	
	
}
