package model;

public class GradeRule {
	String id;
	double cpa;
	int level;
	double mult;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public double getCpa() {
		return cpa;
	}
	public void setCpa(double cpa) {
		this.cpa = cpa;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public double getMult() {
		return mult;
	}
	public void setMult(double mult) {
		this.mult = mult;
	}
	
	
}
